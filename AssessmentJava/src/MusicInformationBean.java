
public class MusicInformationBean {

	private int Song_ID;
	private String Song_Title;
	private String Artist_Name;
	private String Album_Name;
	private String Song_Location;
	private String Description;
	public int getSong_ID()//Getters method
	{
		return Song_ID;
	}
	public void setSong_ID(int Song_ID) //Setters method
	{
		this.Song_ID = Song_ID;
	}
	public String getSong_Title() {
		return Song_Title;
	}
	public void setSong_Title(String Song_Title) {
		this.Song_Title = Song_Title;
	}
	public String getArtist_Name() {
		return  Artist_Name;
	}
	public void setArtist_Name(String  Artist_Name) {
		this.Artist_Name =  Artist_Name;
	}
	public String getAlbum_Name() {
		return Album_Name;
	}
	public void setAlbum_Name(String Album_Name) {
		this.Album_Name = Album_Name;
	}
	public String getSong_Location() {
		return Song_Location;
	}
	public void setSong_Location(String Song_Location) {
		this.Song_Location = Song_Location;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String Description) {
		this.Description = Description;
	}
	
	
}//END OF MusicInformationBean Class
	
